    public void setTextFieldText(String txt1) {
        jLabel_projectID.setText(txt1);
        int projectID = Integer.valueOf(txt1);

        try {
            FileReader reader = new FileReader(JSON_file);

            //Java object
            Object obj = parser.parse(reader);

            //JSON object
            JSONObject jsonObject = (JSONObject) obj;

            JSONArray array = (JSONArray) jsonObject.get("projects");

            JSONObject project = (JSONObject) array.get(projectID - 1);
            long project_ID = (long) project.get("id");
            String ID = String.valueOf(project_ID);
            String project_name = (String) project.get("name");
            JSONArray array1 = (JSONArray) project.get("issues");

            for (int j = 0; j < array1.size(); j++) {
                JSONObject issues = (JSONObject) array1.get(j);
            }
            int issueID = array1.size();
            jTextField_issueID.setText(String.valueOf(++issueID));

            reader.close();
        } catch (Exception e) {
            e.printStackTrace();    //shows neither the line number where the error occurred nor the functions that were executed
        }

    }

