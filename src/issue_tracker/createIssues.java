    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        if ( jTextField_title.getText().isEmpty() || jTextArea_descriptionText.getText().isEmpty()) {           
            JOptionPane.showMessageDialog(null, "Issue title and Issue description cannot be blank!");
        }  
        
        else{
        JSONObject object = null;
        try {

            object = (JSONObject) parser.parse(new FileReader(JSON_file));

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Error!!");
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        JSONArray projects = (JSONArray) object.get("projects");
        int s = Integer.valueOf(jLabel_projectID.getText());
        JSONObject project = ((JSONObject) projects.get(s - 1));
        JSONArray issues = (JSONArray) project.get("issues");
        
        Map<String, Object> newIssue = new LinkedHashMap<>();

        for (int i = 0; i < issues.size(); i++) {
            JSONObject issue = ((JSONObject) issues.get(i));
            long issueID = (long) issue.get("id");
            newIssue.put("id", ++issueID);
        }
        
        newIssue.put("title", jTextField_title.getText());
        newIssue.put("status", jTextField_status.getText());
        JSONArray tag = new JSONArray();
        tag.add(jTextField_tag.getText());
        newIssue.put("tag", tag);
        newIssue.put("descriptionText", jTextArea_descriptionText.getText());
        newIssue.put("createdBy", jTextField_createdBy.getText());
        newIssue.put("assignee", jTextField_assignee.getText());
              
        long epoch = System.currentTimeMillis()/1000;
        newIssue.put("timestamp", epoch);

        if (jComboBox_priority.getSelectedIndex() == 0) {
            newIssue.put("priority", 1);
        }
        if (jComboBox_priority.getSelectedIndex() == 1) {
            newIssue.put("priority", 2);
        }
        if (jComboBox_priority.getSelectedIndex() == 2) {
            newIssue.put("priority", 3);
        }
        if (jComboBox_priority.getSelectedIndex() == 3) {
            newIssue.put("priority", 4);
        }
        if (jComboBox_priority.getSelectedIndex() == 4) {
            newIssue.put("priority", 5);
        }
        if (jComboBox_priority.getSelectedIndex() == 5) {
            newIssue.put("priority", 6);
        }
        if (jComboBox_priority.getSelectedIndex() == 6) {
            newIssue.put("priority", 7);
        }
        if (jComboBox_priority.getSelectedIndex() == 7) {
            newIssue.put("priority", 8);
        }
        if (jComboBox_priority.getSelectedIndex() == 8) {
            newIssue.put("priority", 9);
        }
        
        issues.add(newIssue);
        try (FileWriter file = new FileWriter("data.json")) {
            file.write(JSONValue.toJSONString(object));

        } catch (IOException ex) {
            ex.printStackTrace();
        }
        JOptionPane.showMessageDialog(null, "Create issue successfully!!");
    }
    }//GEN-LAST:event_jButton2MouseClicked

